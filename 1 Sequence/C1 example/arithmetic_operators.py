# Parentheses ()
result = (2 + 3) * 4
print('Parentheses:', result)  # Output: 20, because (2 + 3) is evaluated first to get 5, then multiplied by 4.

# Exponent **
result = 2 ** 3
print('Exponent: ', result)  # Output: 8, because 2 raised to the power of 3 is 8.

# Unary Operators +x and -x
positive = +5
negative = -5
print('Unary Operators +x:', positive)  # Output: 5
print('Unary Operators -x:', negative)  # Output: -5

# Multiplication *
result = 3 * 4
print('Multiplication', result)  # Output: 12, because 3 multiplied by 4 is 12.

# Real Division /
result = 10 / 4
print('Real Division:', result)  # Output: 2.5, because 10 divided by 4 is 2.5.

# Whole Division //
result = 10 // 4
print('Whole Division 10 // 4:', result)  # Output: 2. 10 divided by 4 is 2 with a remainder of 2, the floor value is 2.
result = 10 // 4.0
print('Whole Division 10 // 4.0:', result)  # Output: 2.0
result = 10.0 // 4
print('Whole Division 10.0 // 4:', result)  # Output: 2.0

# Remainder %
result = 10 % 4
print('Remainder:', result)  # Output: 2, because 10 divided by 4 is 2 with a remainder of 2.


