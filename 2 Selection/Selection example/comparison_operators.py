# x == y: equals
x = 5
y = 5
print(x == y)  # Output: True, because 5 is equal to 5

# x != y: is not equal to
x = 5
y = 3
print(x != y)  # Output: True, because 5 is not equal to 3

# x < y: is less than
x = 3
y = 5
print(x < y)  # Output: True, because 3 is less than 5

# x > y: is greater than
x = 5
y = 3
print(x > y)  # Output: True, because 5 is greater than 3

# x <= y: is less than or equal to
x = 3
y = 3
print(x <= y)  # Output: True, because 3 is equal to 3
x = 2
print(x <= y)  # Output: True, because 2 is less than 3

# x >= y: is greater than or equal to
x = 5
y = 5
print(x >= y)  # Output: True, because 5 is equal to 5
x = 6
print(x >= y)  # Output: True, because 6 is greater than 5


