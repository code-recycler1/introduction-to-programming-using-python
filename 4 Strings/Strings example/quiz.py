word = 'mystery'

print(word[2])       # Output: 's'
print(word[2:])      # Output: 'stery'
print(word[:2])      # Output: 'my'
print(word[2:2])     # Output: ''
print(word[::2])     # Output: 'msey'
print(word[-2])      # Output: 'r'
print(word[-2:])     # Output: 'ry'
print(word[:-2])     # Output: 'myster'
print(word[::-2])    # Output: 'yrtsm'
print(word[-2::-2])  # Output: 'rsym'
print(word[::-1])    # Output: 'yretsym'


