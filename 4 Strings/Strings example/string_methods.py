season = "   summer in the city  "

print(season.capitalize())                   # Output: '   Summer in the city  '
print(season.upper())                        # Output: '   SUMMER IN THE CITY  '
print(season.strip())                        # Output: 'summer in the city'
print(season.lstrip())                       # Output: 'summer in the city  '
print(season.rstrip())                       # Output: '   summer in the city'
print(season.count('m'))                     # Output: 2
print(season.find('me'))                     # Output: 6
print(season.replace('m', 'n'))  # Output: '   sunner in the city  '


