# Tuple example
teaching_days = ('Mon', 'Tue', 'Wed', 'Thu', 'Fri')
lesson_hours = (3, 4.5, 6, 4.5, 3)
lesson_hours_per_day = teaching_days + lesson_hours
print(lesson_hours_per_day)  # Output: ('Mon', 'Tue', 'Wed', 'Thu', 'Fri', 3, 4.5, 6, 4.5, 3)

# List example
greek_letters = ['alpha', 'beta', 'omega']
more_letters = greek_letters * 4
print(more_letters)  # Output: ['alpha', 'beta', 'omega', 'alpha', 'beta', 'omega', 'alpha', 'beta', 'omega', 'alpha', 'beta', 'omega']


